namespace SharpCraft.Sdk;

/// <summary>
/// Defines the six cardinal directions in 3D space.
/// </summary>
public enum Direction
{
    /// <summary>
    /// Positive Y direction.
    /// </summary>
    Up,

    /// <summary>
    /// Negative Y direction.
    /// </summary>
    Down,

    /// <summary>
    /// Negative Z direction.
    /// </summary>
    North,

    /// <summary>
    /// Positive Z direction.
    /// </summary>
    South,

    /// <summary>
    /// Positive X direction.
    /// </summary>
    East,

    /// <summary>
    /// Negative X direction.
    /// </summary>
    West
}
